def populate():
    """Наполняет содержимым ПУСТУЮ базу (таблицы уже созданы). Не удаляет данные."""
    # Администратор по умолчанию
    admin = AdminUser(username="admin", full_name="Администратор сайта")
    admin.set_password(os.environ.get("ABS_ADMIN_PASSWORD", "admin123"))
    db.session.add(admin)

    for i, (k, v, label, group, itype) in enumerate(SETTINGS):
        db.session.add(Setting(key=k, value=v, label=label, group=group,
                               input_type=itype, sort_order=i))

    for p in PAGES:
        db.session.add(Page(**p))
    for s in STATS:
        db.session.add(Stat(**s))
    for a in ADVANTAGES:
        db.session.add(Advantage(**a))
    for s in SERVICES:
        db.session.add(Service(**s))
    for l in LICENSES:
        db.session.add(License(**l))
    for a in ASSETS:
        db.session.add(Asset(**a))
    for (num, name, year, spec, qty) in EQUIPMENT:
        db.session.add(Equipment(num=num, name=name, year=year, spec=spec,
                                 qty=qty, sort_order=num))
    for p in PROJECTS:
        db.session.add(Project(**p))
    for t in TASKS:
        db.session.add(Task(**t))
    for p in PARTNERS:
        db.session.add(Partner(**p))
    for t in TESTIMONIALS:
        db.session.add(Testimonial(**t))

    db.session.commit()


